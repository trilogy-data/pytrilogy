"""TPC-H v3-vs-v4 parity eval.

Mirrors discovery_v4_compare.py but for TPC-H: for each query{NN}.preql /
adhocNN.preql under tests/modeling/tpc_h, generate + execute the final
statement under the v3 planner and under v4 (CONFIG.use_v4_discovery), both on
the duckdb tpc-h dataset, and compare result rows (column-sorted, float-rounded
multiset). v3 is the reference (the suite's tpc_h tests — which pass on main —
validate v3 against canonical PRAGMA tpch). A v4 crash / hang / row diff is a
correctness regression.

    python local_scripts/v4_evals/tpc_h_v4_compare.py            # all queries
    python local_scripts/v4_evals/tpc_h_v4_compare.py 02 10 adhoc03

Needs the sf=0.1 dataset at tests/modeling/tpc_h/memory (generated by running
any tpc_h test once).
"""

from __future__ import annotations

import sys
import traceback
from collections import Counter
from decimal import Decimal
from pathlib import Path
from typing import Any

from trilogy import Dialects, Environment, Executor
from trilogy.constants import CONFIG
from trilogy.dialect.config import DuckDBConfig

REPO = Path(__file__).resolve().parent.parent.parent
TPCH_DIR = REPO / "tests" / "modeling" / "tpc_h"
DATASET = TPCH_DIR / "memory"
OUT_DIR = Path(__file__).resolve().parent / "tpc_h_compare"

# v4-attributable parity failures (crash or wrong rows) — flagged with * in
# the run output. adhoc01 is a fan-out bug the targeted suite tests don't
# catch; this row-level parity check does.
#   18 = fan-out + unapplied HAVING (qty>300)
#   22 = global conditional aggregate (avg ? ...) loses its avg() wrapper
#   adhoc01 = window/ratio computed wrong; adhoc03 = count vs key
KNOWN_FAILING = {
    "18",
    "22",
    "adhoc01",
    "adhoc03",
}


def _round(v: Any) -> Any:
    if isinstance(v, (float, Decimal)):
        return round(v, 6)
    return v


def _normalize(columns: list[str], rows: list[tuple]) -> list[tuple]:
    if not columns:
        return [tuple(_round(v) for v in r) for r in rows]
    order = sorted(range(len(columns)), key=lambda i: columns[i])
    return [tuple(_round(row[i]) for i in order) for row in rows]


def make_engine() -> Executor:
    if not (DATASET / "customer.parquet").exists():
        raise FileNotFoundError(
            f"TPC-H dataset missing at {DATASET}. Run a tpc_h test once to build it."
        )
    env = Environment(working_path=TPCH_DIR)
    engine: Executor = Dialects.DUCK_DB.default_executor(
        environment=env, conf=DuckDBConfig()
    )
    engine.execute_raw_sql(f"IMPORT DATABASE '{DATASET}';")
    engine.execute_raw_sql("INSTALL tpch; LOAD tpch;")
    engine.execute_raw_sql("SET enable_progress_bar=false;")
    return engine


def _run(text: str, v4: bool) -> tuple[list[tuple] | None, str]:
    # Fresh engine per run: isolates persisted tables (e.g. adhoc06's
    # dashboard_agg_1) and guarantees no v3 query node bleeds into the v4 run.
    CONFIG.use_v4_discovery = v4
    try:
        engine = make_engine()
        # Execute every generated statement in order (persists create their
        # tables) and take the final statement's rows.
        statements = engine.generate_sql(text)
        cur = None
        for sql in statements:
            cur = engine.execute_raw_sql(sql)
        if cur is None:
            return [], ""
        columns = list(cur.keys())
        rows = [tuple(r) for r in cur.fetchall()]
        return _normalize(columns, rows), ""
    except Exception:
        return None, traceback.format_exc()
    finally:
        CONFIG.use_v4_discovery = False


def _err_line(tb: str) -> str:
    lines = [ln.strip() for ln in tb.strip().splitlines() if ln.strip()]
    for ln in reversed(lines):
        if ("Error" in ln or "Exception" in ln) and "sqlalche.me" not in ln:
            return ln
    return lines[-1] if lines else ""


def run_one(qid: str) -> dict:
    path = TPCH_DIR / f"query{qid}.preql"
    if not path.exists():
        path = TPCH_DIR / f"{qid}.preql"
    text = path.read_text()
    v3, v3_err = _run(text, v4=False)
    v4, v4_err = _run(text, v4=True)
    if v3_err:
        status = "v3_error"
    elif v4_err:
        status = "v4_error"
    elif Counter(v3) == Counter(v4):
        status = "match"
    else:
        status = "mismatch"
    return {
        "qid": qid,
        "status": status,
        "v3_rows": None if v3 is None else len(v3),
        "v4_rows": None if v4 is None else len(v4),
        "v3_err": v3_err,
        "v4_err": v4_err,
        "_v3": v3,
        "_v4": v4,
    }


def _diff(v3: list[tuple], v4: list[tuple]) -> str:
    c3, c4 = Counter(v3), Counter(v4)
    out = []
    only3 = list((c3 - c4).items())[:4]
    only4 = list((c4 - c3).items())[:4]
    if only3:
        out.append("  only in v3: " + "; ".join(f"{n}x{r}" for r, n in only3))
    if only4:
        out.append("  only in v4: " + "; ".join(f"{n}x{r}" for r, n in only4))
    return "\n".join(out)


def all_ids() -> list[str]:
    ids = [f"{i:02d}" for i in range(1, 23)]
    ids += sorted(p.stem for p in TPCH_DIR.glob("adhoc*.preql"))
    return ids


def main(argv: list[str]) -> int:
    ids = argv or all_ids()
    ids = [i if i.startswith("adhoc") or len(i) == 2 else i.zfill(2) for i in ids]
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    results = []
    for qid in ids:
        path = TPCH_DIR / f"query{qid}.preql"
        if not path.exists():
            path = TPCH_DIR / f"{qid}.preql"
        if not path.exists():
            print(f"[skip] {qid}")
            continue
        if "import cache" in path.read_text():
            # cache-dependent (needs cache_warm.preql to build dashboard_agg_1
            # first); not reproducible by a single-file run.
            print(f"[skip] {qid} (cache-dependent)")
            continue
        r = run_one(qid)
        results.append(r)
        flag = "*" if qid in KNOWN_FAILING else " "
        print(f"{flag}[{r['status']:>9}] {qid}  (v3={r['v3_rows']} v4={r['v4_rows']})")
        if r["status"] == "v4_error":
            print("   v4: " + _err_line(r["v4_err"]))
        elif r["status"] == "mismatch":
            print(_diff(r["_v3"], r["_v4"]))
        elif r["status"] == "v3_error":
            print("   v3: " + _err_line(r["v3_err"]))

    counts = Counter(r["status"] for r in results)
    lines = ["# TPC-H v3-vs-v4 parity", ""]
    for st in ("match", "mismatch", "v4_error", "v3_error"):
        if counts[st]:
            lines.append(f"- {st}: {counts[st]}")
    lines.append("")
    lines.append("| Query | Status | v3 rows | v4 rows |")
    lines.append("| --- | --- | --- | --- |")
    for r in results:
        lines.append(
            f"| {r['qid']} | {r['status']} | {r['v3_rows']} | {r['v4_rows']} |"
        )
    (OUT_DIR / "SUMMARY.md").write_text("\n".join(lines), encoding="utf-8")
    match = counts["match"]
    print(f"\n{match}/{len(results)} parity  ->  {OUT_DIR / 'SUMMARY.md'}")
    return 0 if match == len(results) else 1


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
